public class Oper3 {
    public static void main(String args[]) {
        if(true && true){ //jika hasil yang di kondisikan adalah true dan true
            System.out.println(true && true);//maka outputnya adalah true
        }
        //true = true and true
        if(true & true){ //jika hasil yang dikondisikan adalah true & true
            System.out.println(true & false); //maka outputnya adalah false
        }
        //true & true
        if(true){ //jika hasil dari yang dikondisikan adalah trus
            System.out.println(true); //maka outpunya adalah true
        }
        //true
        if(true || true){ //jika hasil dari yang dikondisikan adalah true atau true
            System.out.println(true); //maka outputnya adalah true
        }
        //true = true or true
        if(true|false){ //jika hasil dari yang dikondisikan adalah true atau false
            System.out.println(true|false); //maka outputnya adalah true
        }
        //true|false
    }
}
