//Efek dari operator ++
public class Incr {
    public static void main(String[] args) {
        int i, j;
        //deklarasi variabel i dan j bertipe data integer
        i = 3;
        //memberikan value pada variabel i
        j = i++;
        //memberikan value pada variabel j 
        System.out.println("Nilai i " + (++i) + "\nNilai j : " + j);
        //perintah output teks, hasil dari ++1, teks, dan hasil dari variabel j
    }
}
