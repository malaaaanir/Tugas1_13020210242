public class Ekspresi{
    public static void main(String[] args) {
        int x = 1; 
        //variabel x bertipe data integer dengan value 1
        int y = 2;
        //variabel y bertipe data integer dengan value 2
        /* ALGORITMA */
        System.out.print("X = " + x + "\n");
        //perintah output teks, value dari variabel x, dan teks
        System.out.print("Y = " + y + "\n");
        //perintah output teks, value dari variabel y, dan teks
        System.out.print("Hasil Ekspresi = (x<y)? x:y = " + ((x<y) ? x:y));
        //perintah output teks, value dari statement variabel x dan y
        /*Gunakan dalam kurung "Statement dan kondisi" untuk
        menyatakan satu kesatuan pernyataan*/        
    }
}
