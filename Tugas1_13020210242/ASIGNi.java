/**Deskripsi
Program ini berisi contoh sederhana untuk mendefinisikan
variabel-variabel bilangan bulat (short int, int, long int)
karakter, bilangan riil, karakter rill. */
public class ASIGNi {
    public static void main(String args[]) {
        short ks = 1;
        //variabel ks bertipe data short dengan value 1
        int ki = 1;
        //variabel ki bertipe data integer dengan value 1
        long kl = 10000;
        //variabel kl bertipe data long dengan value 10000
        char c = 64;
        //variabel c bertipe data char dengan value 64
       
        //Inisialisasi karakter dengan integer
       char cl = 'Z';
       //variabel cl bertipe data char dengan value Z
       
        //Inisialisasi karakter dengan karakter
       double x = 50.2f;
       //variabel x bertipe data double dengan value 50.2
       float y = 50.2f;
       //variabel y bertipe data float dengan value 50.2
       
       //Algoritma
       //Penulisan karakter sebagai karakter
       System.out.println("Karakter = " + c);
       //perintah output teks dan value dari variabel c
       System.out.println("Karakter = " + cl);
       //perintah output teks dan value dari variabel cl
       
       //Penulisan karakter sebagai integer
       System.out.println("Karakter = " + c);
       //perintah output teks dan value dari variabel c
       System.out.println("Karakter = " + cl);
       //perintah output teks dan value dari variabel cl
       
       System.out.println("Bilangan Integer (short) = " + ks);
       //petrintah output teks dan value dari variabel ks
       System.out.println("\t(Int) = " + ki);
       //perintah output teks dan value dari variabel ki
       System.out.println("\t (Long) = " + kl);
       //perintah output teks dan value dari variabel kl
       System.out.println("Bilangan Real X = " + x);
       //perintah output teks dan value dari variabel x
       System.out.println("Bilangan Real Y = " + y);
       //perintah output teks dan value dari variabel y
    }
}
